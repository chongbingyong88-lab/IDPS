'use client'

import { Database } from 'lucide-react'
import {
  Bar,
  BarChart,
  CartesianGrid,
  Legend,
  ResponsiveContainer,
  Tooltip,
  XAxis,
  YAxis,
} from 'recharts'
import benchmark from '@/data/benchmark-results.json'

const MODEL_COLORS: Record<string, string> = {
  rf: 'var(--chart-1)',
  svm: 'var(--chart-2)',
  xgb: 'var(--chart-3)',
}

function pct(n: number) {
  return `${(n * 100).toFixed(2)}%`
}

/**
 * Offline benchmark results from the Python pipeline (`ml/run_pipeline.py`),
 * which trains and compares Random Forest, SVM and XGBoost on the UNSW-NB15
 * dataset per the Assignment 1 methodology. This panel only presents the
 * generated `data/benchmark-results.json` — no ML runs in the browser.
 */
export function BenchmarkPanel() {
  const chartData = [
    { metric: 'Accuracy', key: 'accuracy' },
    { metric: 'Precision', key: 'precision' },
    { metric: 'Recall', key: 'recall' },
    { metric: 'F1', key: 'f1' },
    { metric: 'ROC-AUC', key: 'rocAuc' },
  ].map(({ metric, key }) => {
    const row: Record<string, string | number> = { metric }
    for (const m of benchmark.models) {
      row[m.label] = (m as Record<string, unknown>)[key] as number
    }
    return row
  })

  return (
    <div className="rounded-lg border border-border bg-card p-4">
      <div className="mb-1 flex items-center gap-2">
        <Database className="size-4 text-primary" aria-hidden="true" />
        <h2 className="text-sm font-semibold">
          Offline Benchmark — {benchmark.dataset} (RF vs SVM vs XGBoost)
        </h2>
      </div>
      <p className="mb-3 text-[11px] text-muted-foreground">
        Supervised models trained on {benchmark.records.toLocaleString()} labeled
        flow records ({benchmark.featuresEncoded} encoded features,{' '}
        {benchmark.featuresSelected} selected via mutual information), evaluated on
        a stratified {benchmark.testRecords.toLocaleString()}-record test set.
        Generated by <code className="font-mono">ml/run_pipeline.py</code> on{' '}
        {benchmark.generatedAt}.
      </p>

      <div className="grid gap-4 md:grid-cols-2">
        <div className="h-56">
          <ResponsiveContainer width="100%" height="100%">
            <BarChart data={chartData} margin={{ left: -18, right: 8, top: 4 }}>
              <CartesianGrid stroke="var(--border)" vertical={false} />
              <XAxis
                dataKey="metric"
                tick={{ fontSize: 10, fill: 'var(--muted-foreground)' }}
                tickLine={false}
                axisLine={false}
              />
              <YAxis
                domain={[0.8, 1]}
                tick={{ fontSize: 10, fill: 'var(--muted-foreground)' }}
                tickFormatter={(v: number) => v.toFixed(2)}
                tickLine={false}
                axisLine={false}
              />
              <Tooltip
                formatter={(value) =>
                  typeof value === 'number' ? value.toFixed(4) : value
                }
                cursor={{ fill: 'var(--secondary)' }}
                contentStyle={{
                  backgroundColor: 'var(--popover)',
                  border: '1px solid var(--border)',
                  borderRadius: 8,
                  fontSize: 12,
                  color: 'var(--popover-foreground)',
                }}
              />
              <Legend wrapperStyle={{ fontSize: 11 }} />
              {benchmark.models.map((m) => (
                <Bar
                  key={m.id}
                  dataKey={m.label}
                  fill={MODEL_COLORS[m.id] ?? 'var(--chart-4)'}
                  radius={[3, 3, 0, 0]}
                  isAnimationActive={false}
                />
              ))}
            </BarChart>
          </ResponsiveContainer>
        </div>

        <div className="overflow-x-auto">
          <table className="w-full text-left font-mono text-[11px]">
            <thead>
              <tr className="border-b border-border text-muted-foreground">
                <th className="py-1.5 pr-2 font-medium">Model</th>
                <th className="py-1.5 pr-2 font-medium">Acc</th>
                <th className="py-1.5 pr-2 font-medium">F1</th>
                <th className="py-1.5 pr-2 font-medium">FPR</th>
                <th className="py-1.5 pr-2 font-medium">Size</th>
                <th className="py-1.5 font-medium">Latency</th>
              </tr>
            </thead>
            <tbody>
              {benchmark.models.map((m) => (
                <tr key={m.id} className="border-b border-border/50">
                  <td className="py-1.5 pr-2">
                    <span
                      className="mr-1.5 inline-block size-2 rounded-full align-middle"
                      style={{ backgroundColor: MODEL_COLORS[m.id] }}
                    />
                    {m.label}
                  </td>
                  <td className="py-1.5 pr-2 tabular-nums">{pct(m.accuracy)}</td>
                  <td className="py-1.5 pr-2 tabular-nums">{pct(m.f1)}</td>
                  <td className="py-1.5 pr-2 tabular-nums">{pct(m.fpr)}</td>
                  <td className="py-1.5 pr-2 tabular-nums">
                    {m.modelSizeKb >= 1024
                      ? `${(m.modelSizeKb / 1024).toFixed(1)} MB`
                      : `${Math.round(m.modelSizeKb)} KB`}
                  </td>
                  <td className="py-1.5 tabular-nums">{m.latencyUs.toFixed(1)} µs</td>
                </tr>
              ))}
            </tbody>
          </table>
          <p className="mt-2 text-[10px] text-muted-foreground">
            Top predictive features (mutual information + SHAP):{' '}
            {benchmark.topFeatures.slice(0, 6).join(', ')}. Full figures and
            per-attack-family breakdown in <code>ml/results/</code>.
          </p>
        </div>
      </div>
    </div>
  )
}

"""Dataset-explanation figures for the report and live presentation.

Produces five additional figures (fig10-fig14) that explain the dataset and
every preprocessing step visually - complementing the model-result figures
generated by run_pipeline.py.

Usage:  python make_dataset_figures.py   (after download_data.py)
"""

import json

import numpy as np
import pandas as pd
import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
from matplotlib.patches import FancyArrowPatch, FancyBboxPatch

from sklearn.model_selection import train_test_split

from src import config, evaluate, preprocess

C = config.COLORS
BENIGN, ATTACK = "#008300", "#2a78d6"   # matches fig1 (green = benign)


def _save(fig, name):
    config.FIGURES_DIR.mkdir(parents=True, exist_ok=True)
    fig.savefig(config.FIGURES_DIR / name, bbox_inches="tight")
    plt.close(fig)
    print(f"  wrote {name}")


def fig10_pipeline(info: dict) -> None:
    """Step-by-step preprocessing flow with record/feature counts."""
    steps = [
        ("Raw dataset", f"{info['rows_raw']:,} records\n45 columns"),
        ("Step 1 — Clean", f"drop id, dedup\n−{info['duplicates_removed']:,} rows "
                           f"→ {info['rows_clean']:,}"),
        ("Step 2 — Encode", f"one-hot proto/service/state\n42 raw → {info['n_features_encoded']} encoded"),
        ("Step 3 — Normalize", "StandardScaler\n(fitted on train only)"),
        ("Step 4 — Split 70/30", f"stratified on attack_cat\n{info['rows_train']:,} train / "
                                 f"{info['rows_test']:,} test"),
        ("Step 5 — Balance", "class weights /\nscale_pos_weight"),
        ("Step 6 — Select", f"mutual information\ntop {info['n_features_selected']} "
                            f"of {info['n_features_encoded']}"),
    ]
    fig, ax = plt.subplots(figsize=(11, 3.6))
    ax.set_xlim(0, 100)
    ax.set_ylim(0, 10)
    ax.axis("off")
    # two rows: 4 boxes on top, 3 below
    positions = [(1, 5.6), (26, 5.6), (51, 5.6), (76, 5.6), (76, 0.6), (51, 0.6), (26, 0.6)]
    w, h = 22, 3.4
    for (x, y), (title, body) in zip(positions, steps):
        ax.add_patch(FancyBboxPatch((x, y), w, h, boxstyle="round,pad=0.35",
                                    facecolor="#e9f1fb", edgecolor=C["rf"], linewidth=1.2))
        ax.text(x + w / 2, y + h - 0.85, title, ha="center", fontsize=10,
                fontweight="bold", color=C["ink"])
        ax.text(x + w / 2, y + h / 2 - 0.85, body, ha="center", va="center",
                fontsize=8, color=C["ink2"])
    arrows = [((24.2, 7.3), (25.6, 7.3)), ((49.2, 7.3), (50.6, 7.3)),
              ((74.2, 7.3), (75.6, 7.3)), ((87, 5.2), (87, 4.4)),
              ((75.6, 2.3), (74.2, 2.3)), ((50.6, 2.3), (49.2, 2.3))]
    for a, b in arrows:
        ax.add_patch(FancyArrowPatch(a, b, arrowstyle="-|>", mutation_scale=16,
                                     color=C["ink2"], linewidth=1.4))
    ax.set_title("Preprocessing pipeline — UNSW-NB15 (Assignment 1 workflow, steps 1–3)",
                 loc="left", fontsize=11.5, color=C["ink"])
    _save(fig, "fig10_preprocessing_pipeline.png")


def fig11_split_composition(y_tr, y_te) -> None:
    """Class composition of the stratified train/test split."""
    fig, ax = plt.subplots(figsize=(7, 3.4))
    splits = [("Train (70%)", y_tr), ("Test (30%)", y_te)]
    for i, (label, y) in enumerate(splits):
        n_b, n_a = int((y == 0).sum()), int((y == 1).sum())
        total = n_b + n_a
        ax.barh(label, n_b, color=BENIGN, height=0.52)
        ax.barh(label, n_a, left=n_b, color=ATTACK, height=0.52)
        ax.text(n_b / 2, i, f"benign\n{n_b:,} ({n_b/total:.1%})", ha="center",
                va="center", fontsize=9, color="#ffffff", fontweight="bold")
        ax.text(n_b + n_a / 2, i, f"attack\n{n_a:,} ({n_a/total:.1%})", ha="center",
                va="center", fontsize=9, color="#ffffff", fontweight="bold")
    ax.set_xlabel("Records")
    ax.set_title("Stratified 70/30 split preserves the class balance", loc="left",
                 fontsize=11, color=C["ink"])
    ax.grid(axis="y", visible=False)
    _save(fig, "fig11_split_composition.png")


def fig12_feature_distributions(df: pd.DataFrame) -> None:
    """Why the top features discriminate: distributions by class."""
    feats = [
        ("sttl", "sttl (source→dest TTL)", False),
        ("dbytes", "dbytes (dest bytes, log scale)", True),
        ("smean", "smean (mean packet size src)", True),
        ("rate", "rate (packets/s, log scale)", True),
    ]
    fig, axes = plt.subplots(2, 2, figsize=(10, 6))
    for ax, (col, label, log) in zip(axes.ravel(), feats):
        for cls, color, name in ((0, BENIGN, "benign"), (1, ATTACK, "attack")):
            vals = df.loc[df["label"] == cls, col]
            vals = np.log1p(vals) if log else vals
            ax.hist(vals, bins=60, density=True, alpha=0.55, color=color, label=name)
        ax.set_xlabel(("log1p " if log else "") + label, fontsize=9)
        ax.set_ylabel("density", fontsize=9)
        ax.tick_params(labelsize=8)
        ax.legend(fontsize=8.5, frameon=False)
    fig.suptitle("Top discriminative features — benign vs attack distributions",
                 x=0.01, ha="left", fontsize=11.5, color=C["ink"])
    fig.tight_layout(rect=[0, 0, 1, 0.95])
    _save(fig, "fig12_feature_distributions.png")


def fig13_service_composition(df: pd.DataFrame) -> None:
    """Attack share within the most common service categories."""
    top = df["service"].value_counts().head(8).index
    fig, ax = plt.subplots(figsize=(7.5, 4.2))
    rows = []
    for svc in top:
        sub = df[df["service"] == svc]
        rows.append((svc, len(sub), float((sub["label"] == 1).mean())))
    rows.sort(key=lambda r: r[1])
    for i, (svc, n, atk) in enumerate(rows):
        ax.barh(i, 1 - atk, color=BENIGN, height=0.6)
        ax.barh(i, atk, left=1 - atk, color=ATTACK, height=0.6)
        ax.text(1.01, i, f"{n:,} flows", va="center", fontsize=8, color=C["ink2"])
        if atk > 0.08:
            ax.text(1 - atk + atk / 2, i, f"{atk:.0%}", ha="center", va="center",
                    fontsize=8.5, color="#ffffff", fontweight="bold")
    ax.set_yticks(range(len(rows)), [r[0] for r in rows])
    ax.set_xlim(0, 1.14)
    ax.set_xlabel("share of flows (green = benign, blue = attack)")
    ax.set_title("Attack share by service category (top 8 services)", loc="left",
                 fontsize=11, color=C["ink"])
    ax.grid(axis="y", visible=False)
    _save(fig, "fig13_service_composition.png")


def fig14_correlation(df: pd.DataFrame, selected: list[str]) -> None:
    """Correlation structure of the 20 selected (all-numeric) features."""
    corr = df[selected].corr(numeric_only=True)
    from matplotlib.colors import LinearSegmentedColormap
    cmap = LinearSegmentedColormap.from_list(
        "div", ["#0d366b", "#3987e5", "#f0efec", "#e66767", "#8c1d1d"])
    fig, ax = plt.subplots(figsize=(8.2, 7))
    im = ax.imshow(corr, cmap=cmap, vmin=-1, vmax=1)
    ax.set_xticks(range(len(selected)), selected, rotation=90, fontsize=8)
    ax.set_yticks(range(len(selected)), selected, fontsize=8)
    ax.grid(visible=False)
    fig.colorbar(im, ax=ax, shrink=0.8, label="Pearson correlation")
    ax.set_title("Correlation of the 20 selected features", loc="left",
                 fontsize=11, color=C["ink"])
    _save(fig, "fig14_feature_correlation.png")


def main() -> None:
    evaluate.apply_style()
    print("Building dataset figures ...")
    df, info = preprocess.clean(preprocess.load_raw())

    # reproduce the pipeline's split/feature bookkeeping for the flow figure
    metrics = json.loads(config.METRICS_JSON.read_text())
    d = metrics["dataset"]
    info.update({k: d[k] for k in (
        "n_numeric", "n_categorical", "n_features_encoded", "n_features_selected",
        "rows_train", "rows_test")})
    selected = d["selected_features"]

    _, _, y_tr, y_te = train_test_split(
        df.drop(columns=["label"]), df["label"].astype(int),
        test_size=config.TEST_SIZE, stratify=df["attack_cat"],
        random_state=config.SEED)

    fig10_pipeline(info)
    fig11_split_composition(y_tr, y_te)
    fig12_feature_distributions(df)
    fig13_service_composition(df)
    fig14_correlation(df, selected)
    print("Done.")


if __name__ == "__main__":
    main()
